int main()
{
	int i;

	// copy font to RAM buffer
	memcpy(Font_Copy, FontBold8x8, sizeof(FontBold8x8));

	// initialize videomode
	Video(DEV_VGA, RES_VGA, FORM_MTEXT8, TextBuf, Font_Copy);

	// print info
	PrintText(CompTxt);
	DecNum(NumBuf, clock_get_hz(clk_sys)/1000000);
	PrintText(NumBuf);

	// main loop
	while (True)
	{
		// calculate
		Calc();

		// display the number
		u32* s = &Acu[1];
		u32 n;
		Bool ok = True;
		u8 ch, ch2;
		int k = 2;
		int ii = LEN-2;
		if (ii > (TEXTSIZE-NUMBEG)/8) ii = (TEXTSIZE-NUMBEG)/8;
		u8* b = &TextBuf[NUMBEG];
		for (i = 1; i <= ii; i++)
		{
			n = *s++;

			ch = n/10000000 + '0';
			*b++ = ch;
			if (Sample[k++] != ch) ok = False;

			ch = (n/1000000) % 10 + '0';
			*b++ = ch;
			if (Sample[k++] != ch) ok = False;

			ch = (n/100000) % 10 + '0';
			*b++ = ch;
			if (Sample[k++] != ch) ok = False;

			ch = (n/10000) % 10 + '0';
			*b++ = ch;
			if (Sample[k++] != ch) ok = False;

			ch = (n/1000) % 10 + '0';
			*b++ = ch;
			if (Sample[k++] != ch) ok = False;

			ch = (n/100) % 10 + '0';
			*b++ = ch;
			if (Sample[k++] != ch) ok = False;

			ch = (n/10) % 10 + '0';
			*b++ = ch;
			if (Sample[k++] != ch) ok = False;

			ch = n % 10 + '0';
			*b++ = ch;
			if (Sample[k++] != ch) ok = False;
		}

		// display result
		memcpy(TextBuf, ok ? OkTxt : ErrTxt, sizeof(OkTxt)-1);
	}
}
